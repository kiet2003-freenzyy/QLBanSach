    private void btn_DangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DangNhapActionPerformed
        NguoiDungDao nd = new NguoiDungDao();
        NguoiDung nd1 = nd.dangnhap(jTextField1.getText(), jTextField2.getText());
        if (nd1 == null) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            TenNV = nd1.getTennv();
            Role = nd1.getRole();
            frmGiaoDienChinh show = new frmGiaoDienChinh();
            this.dispose();
            show.setVisible(true);

            if (Role.equalsIgnoreCase("Nhan Vien")) {
                show.disableNhanVienFeatures();
                show.disableThongKeFeatures();
            }
        }
    }//GEN-LAST:event_btn_DangNhapActionPerformed

